# Capture function
capture() {
    local url=$1
    local name=$2
    echo "üì∏ $name"
    echo "   URL: $url"
    
    $CHROME --headless --disable-gpu \
        --window-size=$WIDTH,$HEIGHT \
        --screenshot="$name.png" \
        --virtual-time-budget=8000 \
        "$url" 2>/dev/null
    
    if [ -f "$name.png" ]; then
        SIZE=$(du -h "$name.png" | cut -f1)
        echo "   ‚úÖ Saved: $name.png ($SIZE)"
    else
        echo "   ‚ùå Failed"
    fi
    echo ""
}

