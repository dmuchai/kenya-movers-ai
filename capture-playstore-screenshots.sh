# Check for Chrome
CHROME=""
if command -v google-chrome &> /dev/null; then
    CHROME="google-chrome"
elif command -v chromium-browser &> /dev/null; then
    CHROME="chromium-browser"
else
    echo "‚ùå Chrome not found"
    exit 1
fi

# Create directory
mkdir -p screenshots/playstore
